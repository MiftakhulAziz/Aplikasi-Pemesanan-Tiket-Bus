    private void Tampil(){
       
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Kode Jadwal");
        model.addColumn("Hari Tanggal");
        model.addColumn("Jam");
        model.addColumn("Kode Lintasan");
        
        try {
            String sql = "select * from jadwal";
            st = conn.createStatement();
            rs = st.executeQuery(sql);
            
            int no=0;
            
            while(rs.next()){
                no++;
                model.addRow(new Object[]{
                     rs.getString("id_jadwal"), rs.getString("hari_tgl"), 
                     rs.getString("jam"), rs.getString("id_lintasan")
                });
            } jTable2.setModel(model);
              jTable2.setShowGrid(true);
        }catch(Exception e) {
            System.out.println(e.getMessage());
        }
    }
    
    private void TampilData(){

        DefaultComboBoxModel model = new DefaultComboBoxModel();
                
        try {
            
           String sql = "select id_lintasan from lintasan";
        
            st = conn.createStatement();
            rs = st.executeQuery(sql);   
            int no=0;
            while(rs.next()){
               String aku = rs.getString("id_lintasan");
               model.addElement(aku);
            } jComboBox1.setModel(model);
     
        }catch(Exception e) {
            System.out.println(e.getMessage());
        }
    }
