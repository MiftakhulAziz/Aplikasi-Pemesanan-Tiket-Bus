    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Kode Jadwal");
        model.addColumn("Hari Tanggal");
        model.addColumn("Jam");
        model.addColumn("Kode Lintasan");
        

        try {
            String sql= "select * from jadwal where id_jadwal like '%"+tCari.getText()+"%' or hari_tgl like '%"+tCari.getText()+
            "%' or jam like '%"+tCari.getText()+"%' or id_lintasan like '%"+tCari.getText()+"%'";

            st = conn.createStatement();
            rs = st.executeQuery(sql);

            int no=0;

            while(rs.next()){
                no++;
                model.addRow(new Object[]{
                    rs.getString("id_jadwal"), rs.getString("hari_tgl"), 
                     rs.getString("jam"), rs.getString("id_lintasan")
                });
            }
            jTable2.setModel(model);

        }catch(Exception e){
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

