    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Kode Rute");
        model.addColumn("Asal");
        model.addColumn("Tujuan");
        model.addColumn("Jarak");
        model.addColumn("Waktu Tempuh");
        
        try {
            String sql= "select * from rute where id_rute like '%"+tCari.getText()+
            "%' or asal like '%"+tCari.getText()+
            "%' or tujuan like '%"+tCari.getText()+
            "%' or jarak like '%"+tCari.getText()+
            "%' or waktu_tempuh like '%"+tCari.getText()+"%'";

            st = conn.createStatement();
            rs = st.executeQuery(sql);

            int no=0;

            while(rs.next()){
                no++;
                model.addRow(new Object[]{
                     rs.getString("id_rute"), rs.getString("asal"), 
                     rs.getString("tujuan"), rs.getString("jarak"),
                     rs.getString("waktu_tempuh")
                });
            }
            jTable4.setModel(model);
        }catch(Exception e){
            System.out.println("salah");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

