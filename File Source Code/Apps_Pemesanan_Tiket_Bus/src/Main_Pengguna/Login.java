    private void jPanel8MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel8MouseClicked
        // TODO add your handling code here:
        
        if(user.getText().isEmpty() || pass.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Inputkan username dan password");
        }else{
            pengguna pm = new pengguna();
            List<pengguna> dataLogin=new ArrayList<>();
            dataLogin = pm.cariLogin(user.getText(), pass.getText());
            if(dataLogin.isEmpty()){
                JOptionPane.showMessageDialog(null, "Username dan password Salah");
            }else{
                if(dataLogin.get(0).getHakakses().equals("admin")){
                   Menu_Default mn = new Menu_Default();
                   mn.dispose();
                   Admin ad = new Admin();
                   ad.setVisible(true);
                  // Jadwal_Bus bus = new Jadwal_Bus();
                   //bus.setVisible(true);
                  // adm.setVisible(true);
                }else if(dataLogin.get(0).getHakakses().equals("penumpang")){
                    dispose();
                    isLogged.logged = true;
                    ambilKodeUser = dataLogin.get(0).getKode_user();
                    ambilUsername = dataLogin.get(0).getUsername();
                    ambilPassword = dataLogin.get(0).getPassword();
                    System.out.println(dataLogin.get(0).getUsername());
                    System.out.println(ambilPassword);
                        
                }else{
                    System.out.println("Login Salah");
                }                         
            
            }
       }
        
    }//GEN-LAST:event_jPanel8MouseClicked

